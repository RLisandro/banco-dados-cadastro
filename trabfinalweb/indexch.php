   <h1>Conceito de História</h1>
    <p>A história dos conceitos (em alemão: Begriffsgeschichte) é um campo da História voltado para a análise histórica dos conceitos. Seu surgimento como proposta metodológica remonta aos séculos XVIII e XIX, mas apenas no século XX adquiriu o estatuto de disciplina autônoma.</p>
    <p>Princípios centrais da história dos conceitos incluem a distinção entre conceito e palavra, a utilização dos conceitos de sincronia, diacronia, semasiologia e onomasiologia (importados da linguística) e a formulação do conceito de Sattelzeit, que corresponde ao período histórico equivalente ao advento da modernidade, durante o qual diversos conceitos sociais e políticos passaram por profundas transformações semânticas.</p>
    <p>Projetos importantes nessa área incluem o Dicionário Histórico de Filosofia, o Conceitos Históricos Básicos e o Manual de Conceitos político-sociais Básicos na França. Além disso, outros projetos em história conceitual surgiram em diferentes partes do mundo, como o Iberconceptos, o Projeto de História Conceitual Europeia e o projeto História Conceitual Global da Ásia.</p>
