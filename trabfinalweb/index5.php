<h1>Movimentos da Terra</h1>
    <p>A Terra realiza diversos movimentos, como a rotação e a translação. Vamos entender cada um deles:</p>
    <h2>Rotação</h2>
    <p>A rotação é o movimento que a Terra realiza em torno do seu próprio eixo. Ela ocorre no sentido anti-horário, de oeste para leste, e é responsável pela sucessão de dias e noites.</p>
    <!-- Mais conteúdo sobre a rotação... -->
    <h2>Translação</h2>
    <p>A Translação é o movimento que a Terra realiza em torno do Sol, percorrendo uma órbita elíptica. Esse movimento leva aproximadamente 365 dias e é responsável pelas estações do ano.</p>
