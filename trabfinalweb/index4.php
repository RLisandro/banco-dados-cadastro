    <h1>Teoria da Deriva Continental</h1>
    <p>A deriva continental é uma teoria que versa sobre a evolução das formas das terras emersas ao longo das eras geológicas.</p>
    <p>De acordo com essa teoria, no passado havia apenas um continente chamado <strong>Pangeia</strong>.</p>
    <p>A teoria da Deriva Continental foi inicialmente proposta por Alfred Wegener em 1912. Ele observou evidências como fósseis e grupos de vegetação semelhantes em áreas separadas por oceanos inteiros.</p>
    <p>Wegener defendia que a Pangeia se fragmentou lentamente, formando dois grandes continentes: Laurásia e Gondwana. Posteriormente, novas fragmentações e uniões ocorreram.</p>
    <p>Após a Segunda Guerra Mundial, com tecnologias avançadas, comprovou-se que a crosta terrestre é dividida em placas tectônicas que se movimentam continuamente.</p>
    <p>Os continentes atuais ainda se movem, mas em uma velocidade muito lenta. Daqui a milhões de anos, sua configuração será diferente.</p>
