<h1>Conceito de Geografia</h1>
    <p>A Geografia é uma ciência que tem como objeto principal de estudo o espaço geográfico, correspondendo ao palco das realizações humanas. Desde os primórdios, o homem demonstrou curiosidade em relação aos lugares onde ocorrem as interações humanas e as relações entre o homem e a natureza, principalmente com o intuito de alcançar seus interesses.</p>
    <p>O conhecimento da Terra e de todas as dinâmicas existentes configura-se como um objetivo intrínseco da ciência geográfica. Embora tenha suas raízes desde os primórdios da humanidade, sua condição de ciência consolidou-se com o nascimento da civilização grega. Pensadores como Pitágoras e Aristóteles já tinham convicção acerca da forma esférica do planeta.</p>
    <p>O termo "Geografia" possui diversos significados. De forma genérica, "geo" significa Terra e "grafia" significa descrição, ou seja, a Geografia é a descrição da Terra. Essa ciência descreve todos os elementos contidos na superfície do planeta, como a atmosfera, a hidrosfera e a litosfera, que compõem a biosfera (onde se desenvolve a vida). Além disso, a Geografia estuda a interação desses elementos com os seres vivos.</p>
