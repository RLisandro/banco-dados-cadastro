   <h1>Pré-História</h1>
    <p>A Pré-História foi o período do surgimento e desenvolvimento dos primeiros humanos na Terra. Para sobreviver às intempéries da natureza, eles tiveram que se adaptar ao meio ambiente. Esses grupos eram caçadores, pescadores e coletores, alimentando-se daquilo que estava disponível na natureza.</p>
    <p>Com o passar do tempo, a relação entre ser humano e natureza mudou, pois a humanidade começou a dominar e explorar a natureza. A descoberta e o controle do fogo, bem como o surgimento da agricultura, promoveram significativas mudanças na forma dos hominídeos se organizarem socialmente. A Idade dos Metais mostrou o nível de complexidade do homem primitivo ao usar metais como ferro para a confecção de armas.</p>
    <p>Divisão da Pré-História:</p>
    <ul>
        <li>Paleolítico</li>
        <li>Mesolítico</li>
        <li>Neolítico</li>
        <li>Idade dos Metais</li>
    </ul>
    <h2>Período Paleolítico</h2>
    <p>No Paleolítico, os primeiros seres humanos eram nômades, não tinham local fixo para morar porque estavam se deslocando de um lugar para o outro, procurando melhores condições para viver. Eles praticavam a caça, a pesca e a coleta de frutos. Esse período é conhecido como Idade da Pedra Lascada, por causa da produção de objetos lascados em pedras.</p>
    <p>No final do Paleolítico, ocorreu a descoberta e o controle do fogo. Dessa forma, os humanos poderiam caçar à noite, cozinhar os alimentos, espantar animais selvagens e se proteger do frio.</p>
    <h2>Período Mesolítico</h2>
    <p>Entre o Paleolítico e o Neolítico, o período do Mesolítico foi o momento de transição e de transformações significativas para os hominídeos. A formação geográfica do planeta Terra foi concluída, e as modificações no ambiente promoveram a sedentarização de alguns grupos, fixando-se em determinadas regiões férteis.</p>
    <h2>Período Neolítico</h2>
    <p>Alguns estudiosos consideram o período do Neolítico como uma revolução por conta do aprofundamento das relações entre o ser humano e o meio ambiente. A principal característica desse período da Pré-História foi a prática da agricultura. Os grupos que se sedentarizaram e se tornaram agricultores utilizavam da terra para sobreviver. Eles se tornaram produtores de alimentos. Foi nesse momento que o artesanato se desenvolveu por meio da produção de utensílios domésticos feitos de palha ou argila para armazenar a produção excedente.</p>
    <h2>Idade dos Metais</h2>
    <p>Por fim, o último período da Pré-História é a Idade dos Metais. Por volta do ano 6 mil a.C., os metais foram utilizados para a confecção de armas e outros instrumentos. Surgia a figura do artesão, aquele que trabalhava especificamente na produção de objetos feitos dos metais. Além disso, as primeiras aldeias agrícolas aumentaram a população e se tornaram os primeiros aglomerados urbanos da história.
</p>
