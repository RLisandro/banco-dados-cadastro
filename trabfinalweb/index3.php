            <h1>Principais Tipos de Climas no Mundo</h1>
<p><strong>Equatorial:</strong> Ocorre próximo à linha do equador, com altas temperaturas e grande umidade.</p>
<p><strong>Tropical:</strong> Duas estações bem definidas (seca e chuvosa), com índices pluviométricos de 1.000 a 2.000 mm.</p>
<p><strong>Subtropical:</strong> Amplitude térmica ao longo do ano, chuvas bem distribuídas e ocorrência de neve no inverno.</p>
<p><strong>Temperado Oceânico (Maritimidade):</strong> Invernos menos intensos devido à influência do mar.</p>
<p><strong>Temperado Continental:</strong> Grandes variações de temperatura entre inverno e verão.</p>
<p><strong>Mediterrâneo:</strong> Todas as estações do ano, com verões quentes e invernos chuvosos.</p>
<p><strong>Desértico:</strong> Enorme amplitude térmica e baixos índices pluviométricos (cerca de 250 mm anuais).</p>
<p><strong>Semiárido:</strong> Elevadas temperaturas durante o ano e chuvas irregulares (índices pluviométricos até 600 mm anuais).</p>
<p><strong>Subpolar:</strong> Índices pluviométricos de 200 a 1.000 mm anuais, invernos abaixo de 0°C e verões em torno de 10°C.</p>
<p><strong>Frio de Montanha:</strong> Quanto maior a altitude, menor a temperatura.</p>
<p><strong>Polar:</strong> Constante presença de neve e gelo, com temperaturas sempre abaixo de zero.</p>
