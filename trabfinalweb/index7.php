<h1>Primeira Guerra Mundial</h1>
    <p>A Primeira Guerra Mundial foi um conflito ocorrido entre 1914 e 1918 que envolveu vários países e terminou com a assinatura do Tratado de Versalhes, em 1919.</p>
    <p>A Batalha de Verdun foi uma das mais importantes da Primeira Guerra Mundial e ocorreu em abril de 1916.</p>
    <p>As origens remontam a meados do século XIX, quando as grandes potências europeias disputavam entre si mercado consumidor, matéria-prima e metais preciosos na Ásia e na África. Essa disputa levou à corrida armamentista, ou seja, investimento em armamentos potentes caso houvesse enfrentamento entre essas potências.</p>
    <p>Essa guerra se encerrou logo após a assinatura do Tratado de Versalhes, em 1919. O domínio europeu sobre o mundo deixou grandes consequências, como a ascensão dos Estados Unidos como maior país capitalista e o surgimento do nazifascismo na Alemanha e na Itália.</p>
    <p>Resumo:</p>
    <ul>
        <li>A Primeira Guerra Mundial foi um conflito envolvendo vários países entre 1914 e 1918.</li>
        <li>Desde o século XIX, as potências europeias rivalizavam o domínio das novas colônias na Ásia e África, o que gerou a corrida armamentista.</li>
        <li>A Paz Armada significou o investimento em armamentos no período anterior à guerra.</li>
        <li>Duas alianças foram formadas na guerra: Tríplice Aliança (Alemanha, Itália e Império Austro-Húngaro) e Tríplice Entente (Estados Unidos, Rússia, França e Inglaterra).</li>
        <li>O Brasil participou da guerra após submarinos alemães afundarem navios brasileiros e enviou para a Tríplice Entente auxílio médico e humanitário.</li>
        <li>As consequências da guerra foram a Alemanha sendo acusada de ser a grande culpada pelo conflito, o domínio dos Estados Unidos e o fim da Era dos Impérios.</li>
        <li>A Segunda Guerra Mundial retomou alguns resquícios deixados pela Primeira Guerra Mundial, como a vingança de Adolf Hitler contra os termos do Tratado de Versalhes.</li>
    </ul>
    <p>Antecedentes e motivos da Primeira Guerra Mundial:</p>
    <p>Em meados do século XIX, a Europa atravessava a segunda fase da Revolução Industrial, quando outros países passavam pelo processo de industrialização. As unificações alemã e italiana, ocorridas no mesmo período, acirraram a concorrência dessas potências para expandir o domínio econômico em outras regiões, como a Ásia e a África. Essas unificações se deram por meio de guerras entre as nações europeias.</p>
    <p>O neoimperialismo foi o domínio europeu sobre o continente asiático e africano. Além das questões econômicas, a cultura foi outro fator importante para o domínio europeu nesses continentes. A Europa representava o desenvolvimento e o progresso, enquanto a África e a Ásia eram consideradas “inferiores” e necessitavam desse domínio externo para aderir à civilização.</p>
